#include <iostream>

int main()
{
    std::cout << "Enter two numbers:" << std::endl;
    float a = 0., b = 0.;
    std::cin >> a >> b;
    std::cout << "a + b: " << a + b << std::endl;
    std::cout << "a - b: " << a - b << std::endl;
    std::cout << "a * b: " << a * b << std::endl;
    std::cout << "a / b: " << a / b << std::endl;
}